class MainActivity : ComponentActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContent {
            val navController = rememberNavController()
            AppNavigation(navController)
        }
    }
}

@Composable
fun AppNavigation(navController: NavHostController) {
    NavHost(navController = navController, startDestination = "start") {
        composable("start") { IniciQuiz(navController) }
        composable("questions") { Pregunta(navController) }
        composable("final/{timeElapsed}") { backStackEntry ->
            val timeElapsed =
                backStackEntry.arguments?.getString("timeElapsed")?.toLongOrNull() ?: 0
            ResultActivity(navController, timeElapsed)
        }
    }
}

